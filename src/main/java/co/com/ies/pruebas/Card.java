package co.com.ies.pruebas;

public class Card {
	
	private SuitType suit;
	private ValueType value;
	
	public SuitType getSuit() {
		return suit;
	}
	public void setSuit(SuitType suit) {
		this.suit = suit;
	}
	public ValueType getValue() {
		return value;
	}
	public void setValue(ValueType value) {
		this.value = value;
	}

}
